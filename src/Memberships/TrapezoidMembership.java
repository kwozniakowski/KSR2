package Memberships;

public class TrapezoidMembership {
    private String name;
    private float a,b,c,d;

    public TrapezoidMembership(String name, float a, float b, float c, float d)
    {
        this.name = name;
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }
}
